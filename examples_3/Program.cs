Console.Write("Введите число => ");
string inputNumber = Console.ReadLine();
int number = int.Parse(inputNumber);

if(1 == number) Console.WriteLine("Понедельник");
if(2 == number) Console.WriteLine("Вторник");
if(3 == number) Console.WriteLine("Среда");
if(4 == number) Console.WriteLine("Четверг");
if(5 == number) Console.WriteLine("Пятница");
if(6 == number) Console.WriteLine("Суббота");
if(7 == number) Console.WriteLine("Восресенье");